def translate(text, **kwargs):
    language = get_language() or 'en'
    language = language.split('-')[0]
    translated = TRANSLATIONS.get(language, {}).get(text, text)
    if kwargs:
        try:
            return translated.format(**kwargs)
        except Exception:
            return translated
    return translated
